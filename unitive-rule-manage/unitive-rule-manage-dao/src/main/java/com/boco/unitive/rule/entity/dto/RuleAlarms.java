package com.boco.unitive.rule.entity.dto;

public class RuleAlarms {
	private Integer serFileId;
	private String fileName;
	private String optPerson;
	private String subAlarm;
	private String filterName;
	public Integer getSerFileId() {
		return serFileId;
	}
	public void setSerFileId(Integer serFileId) {
		this.serFileId = serFileId;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getOptPerson() {
		return optPerson;
	}
	public void setOptPerson(String optPerson) {
		this.optPerson = optPerson;
	}
	public String getSubAlarm() {
		return subAlarm;
	}
	public void setSubAlarm(String subAlarm) {
		this.subAlarm = subAlarm;
	}
	public String getFilterName() {
		return filterName;
	}
	public void setFilterName(String filterName) {
		this.filterName = filterName;
	}
	
}
